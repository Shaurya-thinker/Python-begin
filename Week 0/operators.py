#Assignment Operators
x = 10  
y = 5
# Assignment
x = y  # Assigns the value of y to x
# Add and assign
x += y  # Equivalent to x = x + y
# Subtract and assign
x -= y  # Equivalent to x = x - y
# Multiply and assign
x *= y  # Equivalent to x = x * y
# Divide and assign
x /= y  # Equivalent to x = x / y
# Floor divide and assign
x //= y  # Equivalent to x = x // y
# Modulus and assign
x %= y  # Equivalent to x = x % y
# Exponentiate and assign
x **= y  # Equivalent to x = x ** y
